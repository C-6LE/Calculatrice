def puissance(number_1: int | float, number_2: int | float) -> int | float:
    """
    Fonction qui calcule la puissance d' un nombre.

    [Arguments]
    - number_1: int | float = base choisie par l'utilisateur
    - number_2: int | float = exposant choisi par l'utilisateur

    [Return]
    - return number_1 élevé à la puissance number_2
    """
    return number_1 ** number_2