def addition(number_1: int|float, number_2: int|float) :
    """
    Fonction addition qui permet d'additionner 2 nombres et d' obtenir le resultat
    [Arguments]
    - number_1: int|float = premier nombre choisi par l'utilisateur
    - number_2: int|float = deuxième nombre choisi par l'utilisateur
    [Return]
    - return number_1 ajouté à number_2
    """
    return number_1 + number_2

