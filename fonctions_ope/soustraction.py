def soustraction(number_1: int|float, number_2: int|float) -> int|float :
    """
    Fonction soustraction qui permet de soustraire 2 chiffre et obtenir le resultat

    [Arguments]
    - number_1: int|float = nombre 1 choisit par l'utilisateur
    - number_2: int|float = nombre 2 choisit par l'utilisateur
    
    [Return]
    - return number_1 soustrais par number_2
    """
    return number_1-number_2