def racine_carree(number_1: int|float) :
    """
    Fonction  qui permet de calculer la racine carrée d'un nombre donné par l'utilisateur.

    [Arguments]
    - number_1: int|float = nombre choisi par l'utilisateur.

    [Return]
    - return racine carrée de number_1 
    """
    return number_1 ** 0.5